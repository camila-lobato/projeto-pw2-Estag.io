CREATE DATABASE IF NOT EXISTS estagio;
USE estagio;

CREATE TABLE empresas (
  id INT AUTO_INCREMENT PRIMARY KEY,
  nome VARCHAR(255) NOT NULL,
  cnpj VARCHAR(20) NOT NULL UNIQUE,
  email VARCHAR(255) NOT NULL UNIQUE,
  telefone VARCHAR(30),
  site VARCHAR(255),
  descricao TEXT,
  criadoEm DATETIME DEFAULT CURRENT_TIMESTAMP,
  atualizadoEm DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

CREATE TABLE vagas (
  id INT AUTO_INCREMENT PRIMARY KEY,
  titulo VARCHAR(255) NOT NULL,
  descricao TEXT NOT NULL,
  modalidade ENUM('Presencial','HÃ­brido','Remoto') NOT NULL,
  salario DECIMAL(10,2),
  localidade VARCHAR(255),

  empresaId INT NOT NULL,
  criadoEm DATETIME DEFAULT CURRENT_TIMESTAMP,
  atualizadoEm DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,

  FOREIGN KEY (empresaId) REFERENCES empresas(id) ON DELETE CASCADE
);

CREATE TABLE estudantes (
  id INT AUTO_INCREMENT PRIMARY KEY,
  nome VARCHAR(255) NOT NULL,
  email VARCHAR(255) NOT NULL UNIQUE,
  senha VARCHAR(255) NOT NULL,
  curso VARCHAR(255),
  github VARCHAR(255),
  portfolio VARCHAR(255),
  biografia TEXT,
  criadoEm DATETIME DEFAULT CURRENT_TIMESTAMP,
  atualizadoEm DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);




CREATE TABLE candidaturas (
  id INT AUTO_INCREMENT PRIMARY KEY,

  idEstudante INT NOT NULL,
  idVaga INT NOT NULL,
  mensagem TEXT,

  dataCandidatura DATETIME DEFAULT CURRENT_TIMESTAMP,

  FOREIGN KEY (idEstudante) REFERENCES estudantes(id) ON DELETE CASCADE,
  FOREIGN KEY (idVaga) REFERENCES vagas(id) ON DELETE CASCADE
);




